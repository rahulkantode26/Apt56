const fileInput = document.getElementById("file-input");
const dropzone = document.getElementById("dropzone");
const dropzoneEmpty = document.getElementById("dropzone-empty");
const preview = document.getElementById("preview");
const analyzeBtn = document.getElementById("analyze-btn");
const resultEl = document.getElementById("result");
const errorEl = document.getElementById("error");

let selectedFile = null;

function showFile(file) {
  selectedFile = file;
  preview.src = URL.createObjectURL(file);
  preview.hidden = false;
  dropzoneEmpty.hidden = true;
  analyzeBtn.disabled = false;
  resultEl.hidden = true;
  errorEl.hidden = true;
}

fileInput.addEventListener("change", (e) => {
  if (e.target.files.length) showFile(e.target.files[0]);
});

// Drag & drop
["dragover", "dragenter"].forEach((ev) =>
  dropzone.addEventListener(ev, (e) => {
    e.preventDefault();
    dropzone.classList.add("dragover");
  })
);
["dragleave", "drop"].forEach((ev) =>
  dropzone.addEventListener(ev, (e) => {
    e.preventDefault();
    dropzone.classList.remove("dragover");
  })
);
dropzone.addEventListener("drop", (e) => {
  const file = e.dataTransfer.files[0];
  if (file && file.type.startsWith("image/")) showFile(file);
});

function showError(msg) {
  errorEl.textContent = msg;
  errorEl.hidden = false;
  resultEl.hidden = true;
}

function renderResult(data) {
  const badge = document.getElementById("result-badge");
  badge.textContent = data.prediction;
  badge.className = "badge " + (data.prediction.toLowerCase() === "cataract" ? "cataract" : "normal");

  document.getElementById("result-confidence").textContent =
    `${(data.confidence * 100).toFixed(1)}% confidence`;

  const bars = document.getElementById("bars");
  bars.innerHTML = "";
  Object.entries(data.probabilities)
    .sort((a, b) => b[1] - a[1])
    .forEach(([cls, p]) => {
      const row = document.createElement("div");
      row.className = "bar-row";
      row.innerHTML = `
        <span class="bar-label">${cls}</span>
        <span class="bar-track"><span class="bar-fill" style="width:0%"></span></span>
        <span class="bar-pct">${(p * 100).toFixed(0)}%</span>`;
      bars.appendChild(row);
      requestAnimationFrame(() => {
        row.querySelector(".bar-fill").style.width = `${p * 100}%`;
      });
    });

  document.getElementById("explanation").textContent = data.explanation;
  const sourceNotes = {
    local: "Explanation generated by a local AI model.",
    llm: "Explanation generated by an AI model.",
    fallback: "Explanation generated from a template (no AI model available).",
  };
  document.getElementById("source-note").textContent =
    sourceNotes[data.explanation_source] || sourceNotes.fallback;

  resultEl.hidden = false;
  errorEl.hidden = true;
}

analyzeBtn.addEventListener("click", async () => {
  if (!selectedFile) return;
  analyzeBtn.classList.add("loading");
  analyzeBtn.textContent = "Analyzing…";

  try {
    const form = new FormData();
    form.append("file", selectedFile);
    const res = await fetch("/api/predict", { method: "POST", body: form });
    const data = await res.json();
    if (!res.ok) throw new Error(data.detail || "Prediction failed.");
    renderResult(data);
  } catch (err) {
    showError(err.message);
  } finally {
    analyzeBtn.classList.remove("loading");
    analyzeBtn.textContent = "Analyze";
  }
});
